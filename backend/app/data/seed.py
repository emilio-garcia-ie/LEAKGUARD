def incident_to_api(row: Incident) -> dict:
    p = row.payload or {}
    return {
        "id": row.id,
        "date": row.date.isoformat(),
        "actor": row.actor,
        "victim": row.victim,
        "sector": row.sector,
        "country": row.country,
        "riskScore": row.risk_score,
        "confidence": row.confidence,
        "status": row.status,
        "verificationStatus": row.verification_status,
        **p,
    }


async def seed_database(session: AsyncSession) -> None:
    for t in THREATS:
        existing = await session.get(Incident, t["id"])
        if existing:
            continue
        payload = {k: v for k, v in t.items() if k not in ("id", "date", "actor", "victim", "sector", "country", "riskScore", "confidence", "status", "verificationStatus")}
        session.add(
            Incident(
                id=t["id"],
                date=date.fromisoformat(t["date"]),
                actor=t["actor"],
                victim=t["victim"],
                sector=t["sector"],
                country=t["country"],
                risk_score=t["riskScore"],
                confidence=t["confidence"],
                status=t["status"],
                verification_status=t["verificationStatus"],
                payload=payload,
            )
        )

    for a in AUDITS:
        existing_audit = await session.scalar(select(AuditLog.id).filter_by(analyst=a["analyst"], action=a["action"], reason=a["reason"]).limit(1))
        if existing_audit:
            continue
        session.add(AuditLog(analyst=a["analyst"], action=a["action"], reason=a["reason"]))

    await session.commit()

